# Python tipos de datos básicos, incluyendo enteros, flotantes, cadenas y booleanos.

# Enteros (int)
numero = 10
print(numero)  # Imprime: 10

# Flotantes (float)
numero_decimal = 3.14
print(numero_decimal)  # Imprime: 3.14

# Cadenas (str)
texto = "hola mundo"
print(texto)  # Imprime: hola mundo

# Booleanos (bool)
verdadero = True
falso = False
print(verdadero)  # Imprime: True
print(falso)  # Imprime: False

# Python también tiene varios tipos de datos compuestos, incluyendo listas, tuplas y diccionarios.

# Listas
lista = [1, 2, 3, 4, 5]
print(lista)  # Imprime: [1, 2, 3, 4, 5]
print(lista[0])  # Accede al primer elemento de la lista. Imprime: 1

# Tuplas
tupla = (1, 2, 3, 4, 5)
print(tupla)  # Imprime: (1, 2, 3, 4, 5)
print(tupla[0])  # Accede al primer elemento de la tupla. Imprime: 1

# Diccionarios
diccionario = {"clave": "valor", "nombre": "Juan", "edad": 30}
print(diccionario)  # Imprime: {'clave': 'valor', 'nombre': 'Juan', 'edad': 30}
print(diccionario["nombre"])  # Accede al valor asociado a la clave "nombre". Imprime: Juan

# Constantes
PI = 3.14159
print(PI)  # Imprime: 3.14159

