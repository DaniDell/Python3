# Operadores aritméticos
suma = 5 + 3
print(suma)  # Imprime: 8

resta = 5 - 3
print(resta)  # Imprime: 2

multiplicacion = 5 * 3
print(multiplicacion)  # Imprime: 15

division = 5 / 3
print(division)  # Imprime: 1.6666666666666667

modulo = 5 % 3
print(modulo)  # Imprime: 2

exponente = 5 ** 3
print(exponente)  # Imprime: 125

# Operadores de comparación
igual = 5 == 3
print(igual)  # Imprime: False

diferente = 5 != 3
print(diferente)  # Imprime: True

mayor_que = 5 > 3
print(mayor_que)  # Imprime: True

menor_que = 5 < 3
print(menor_que)  # Imprime: False

mayor_o_igual_que = 5 >= 3
print(mayor_o_igual_que)  # Imprime: True

menor_o_igual_que = 5 <= 3
print(menor_o_igual_que)  # Imprime: False

# Operadores lógicos
y_logico = True and False
print(y_logico)  # Imprime: False

o_logico = True or False
print(o_logico)  # Imprime: True

no_logico = not True
print(no_logico)  # Imprime: False